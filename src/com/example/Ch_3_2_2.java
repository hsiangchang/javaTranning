package com.example;

public class Ch_3_2_2 {

	public static void main(String[] args) {

		// switch
		char a = 'a';
		String result = "";
		switch (a) {
		case 'a' :
			result = "很慘" ;
			break;
		case 'b' :
			result = "還好" ;
			break;
		}

		System.out.println(result);

	}

}
